const { withAppBuildGradle } = require("expo/config-plugins");

// Local Windows Android builds only.
//
// The React Native gradle plugin's `Os.cliPath()` rewrites `--entry-file` to a
// path relative to the app dir *on Windows* (to dodge Gradle's space-in-path
// issues on that platform), so Expo's `export:embed` receives a bare
// `index.ts`. Expo forwards that relative entry to Metro unchanged, and in an
// npm-workspace monorepo Metro resolves it against the workspace server root
// instead of the app dir — failing the JS bundle step with
// "Unable to resolve module ./index.ts from <monorepo root>". On Linux/macOS
// `cliPath()` returns the absolute path, so EAS/CI builds never hit this.
//
// Fix: point the gradle plugin's `cliFile` at scripts/metro-embed-cli.cjs, a
// thin wrapper that re-absolutizes `--entry-file` before delegating to
// @expo/cli. `android/` is regenerated by `expo prebuild`, so this rewrite must
// live in a config plugin rather than a hand edit to build.gradle.
//
// Gated to `win32` prebuilds: on other platforms the file is left untouched, so
// cloud builds are byte-for-byte unaffected.
const ACTIVE_CLI_FILE =
  /^([ \t]*)cliFile = new File\(\[.*\]\.execute\(null, rootDir\)\.text\.trim\(\)\)$/m;
const REPLACEMENT = 'cliFile = new File(rootDir, "../scripts/metro-embed-cli.cjs")';

module.exports = function withMetroEmbedCli(config) {
  if (process.platform !== "win32") {
    return config;
  }

  return withAppBuildGradle(config, (cfg) => {
    if (cfg.modResults.language !== "groovy") {
      return cfg;
    }

    const contents = cfg.modResults.contents;
    if (contents.includes("scripts/metro-embed-cli.cjs")) {
      return cfg; // already applied
    }
    if (!ACTIVE_CLI_FILE.test(contents)) {
      throw new Error(
        "with-metro-embed-cli: could not find the `cliFile = new File([...])` line in android/app/build.gradle. The React Native gradle template changed; update this plugin's regex.",
      );
    }

    cfg.modResults.contents = contents.replace(ACTIVE_CLI_FILE, `$1${REPLACEMENT}`);
    return cfg;
  });
};
