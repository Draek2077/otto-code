import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const appRoot = path.resolve(__dirname, "..");
const repoRoot = path.resolve(appRoot, "../..");

const mapPath = path.join(appRoot, "scripts/material-symbols-map.json");
const svgRoot = path.join(repoRoot, "node_modules/@material-symbols/svg-400/outlined");
const output = path.join(appRoot, "src/assets/material-symbol-icons.ts");

const map = JSON.parse(await fs.readFile(mapPath, "utf8"));

function injectCurrentColor(svg) {
  if (svg.includes('fill="currentColor"')) return svg;
  return svg.replace("<svg ", '<svg fill="currentColor" ');
}

const entries = [];
for (const [name, key] of Object.entries(map)) {
  const filePath = path.join(svgRoot, `${key}.svg`);
  let raw;
  try {
    raw = await fs.readFile(filePath, "utf8");
  } catch {
    throw new Error(
      `material-symbols-map.json maps "${name}" -> "${key}", but ${path.relative(repoRoot, filePath)} does not exist`,
    );
  }
  entries.push([name, injectCurrentColor(raw.trim())]);
}

entries.sort(([a], [b]) => a.localeCompare(b));

const body = entries.map(([name, svg]) => `  ${name}: ${JSON.stringify(svg)},`).join("\n");

const contents = `// Generated by packages/app/scripts/generate-material-symbols.mjs from
// @material-symbols/svg-400 (outlined). Do not edit by hand.
//
// To add or change an icon, edit packages/app/scripts/material-symbols-map.json
// and rerun: node packages/app/scripts/generate-material-symbols.mjs

export const MATERIAL_SYMBOL_SVGS: Record<string, string> = {
${body}
};
`;

await fs.writeFile(output, contents);
console.log(`Wrote ${path.relative(repoRoot, output)} (${entries.length} icons)`);
